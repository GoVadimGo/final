while factor < number or factor == number:  # цикл будет повторяться до тех пор , пока значение factor не будет равен меньше
    # number или factor будет равен number
    if number % factor == 0 : # если number делиться без остатка на factor , выводим фактор на экран
        print(factor)
        number = math.floor(number / factor) # делим number на factor и обновляем number
    else:
        factor += 1 # если не делится без остатка, тогда учеличиваем factor на 1 .
        
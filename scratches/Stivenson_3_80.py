# меньшее двух, необходимо вывести соответствующее сообщение об ошибке. Иначе нужно перечислить
# в столбец список простых множителей заданного числа, которые при перемножении дали бы исходное число.

import math

number = int(input('Enter number : '))  # получаем число

if number < 2 :   # если число меньше 2 , продолжаем запрашивать число, пока не будет введено число больше равно 2 .
    while number < 2:
        number = int(input('Enter number : '))



